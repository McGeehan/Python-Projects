#calculate the product of 3 numbers

#get some user input
num1 = float(input("Enter first number: ")) #create a variable, accept input of type float.... a float is a decimal number like 1.2
num2 = float(input("Enter second number: ")) #create second variable
num3 = float(input("Enter third number: "))#accept third variable

product = num1 * num2 * num3 #accept the input of numbers and multiply them together

print("The product is: " + str(product)) #concatenation - combining variables together